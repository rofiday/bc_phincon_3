function sum(a, b) {
  return a + b;
}
function substract(a, b) {
  return a - b;
}
function multiplier(a, b) {
  return a * b;
}
function divider(a, b) {
  return a / b;
}
let resultSum = sum(10, 10);
let resultSubstract = substract(10, 10);
let resultMultiplier = multiplier(10, 10);
let resultDivider = divider(10, 10);

console.log(resultSum);
console.log(resultSubstract);
console.log(resultMultiplier);
console.log(resultDivider);
